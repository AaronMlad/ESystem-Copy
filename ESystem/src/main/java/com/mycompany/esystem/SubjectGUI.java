    private void subjTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_subjTableMouseClicked
        int row = subjTable.rowAtPoint(evt.getPoint());
        if (row >= 0) {
            subjId.setText(subjTable.getValueAt(row,0).toString());
            subjCode.setText(subjTable.getValueAt(row,1).toString());
            subjSched.setText(subjTable.getValueAt(row,2).toString());
            subjUnits.setText(subjTable.getValueAt(row,3).toString());
            subjDesc.setText(subjTable.getValueAt(row,4).toString());
        }
        
        selectedSubjectId = Integer.parseInt(subjTable.getValueAt(row,0).toString());
        showEnrollRec();
    }//GEN-LAST:event_subjTableMouseClicked

