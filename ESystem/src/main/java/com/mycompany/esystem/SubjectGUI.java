    public void showRecords() {
    DefaultTableModel tblmodel = (DefaultTableModel) subjTable.getModel();
    tblmodel.setRowCount(0);
    ESystem show = new ESystem();
    show.DBConnect();
    try {
        String query = "SELECT * FROM Subjects;";
        show.rs = show.st.executeQuery(query);
        while (show.rs.next()) {
            String id = show.rs.getString("subjid");
            String code = show.rs.getString("subjcode");
            String sched = show.rs.getString("subjsched");
            String units = show.rs.getString("subjunits");
            String desc = show.rs.getString("subjdesc");

            String[] item = {id, code, sched, units, desc};
            tblmodel.addRow(item);
            }
        } catch (Exception ex) {
            System.out.println(ex);
        }
    }
    
    public void showEnrollRec() {
        DefaultTableModel model = (DefaultTableModel) classTable.getModel();
        model.setRowCount(0); 
        
        if (selectedSubjectId == -1) {
            return;
        }
        
        ESystem show = new ESystem();
        show.DBConnect();
        try {
            String query = "SELECT * FROM Students JOIN Enroll ON Students.studid = Enroll.studid WHERE Enroll.subjid = " + selectedSubjectId;
            
            show.rs = show.st.executeQuery(query);
            
            while (show.rs.next()) {
                String studentId = show.rs.getString("studid");
                String name = show.rs.getString("studname");
                String address = show.rs.getString("studadd");
                String contact = show.rs.getString("studcontact");
                String gender = show.rs.getString("studgender");
                String yearLevel = show.rs.getString("studyrlvl");
                
                model.addRow(new String[]{studentId, name, address, contact, gender, yearLevel});
            }
            
        } catch (Exception ex) { System.out.println(ex); }
    }
    
